    /**
     * Verifica si el inicio de secion es correcto, si los datos ingresados corresponden a los establecidos, para asi pasar a la siguiente ventana
     */    
    private void inicioSesion() {
        String usurio = txtUsuario.getText();
        String clave = new String(txtClave.getPassword());
        Sesion.setCuenta(cs.inicioSesion(usurio, clave));
        if (Sesion.getCuenta() != null) {
            if (Sesion.getCuenta().getUsuario() != null) {
                Sesion.cargarDatos();
                JOptionPane.showMessageDialog(this, "BIENVENIDO: " + Sesion.getEmpleado().toString() + "\n " 
                        + "Ha iniciado como: " + Sesion.getRol().toString());
                new RegistroEnvios().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Cuenta no activa", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Usuario no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

